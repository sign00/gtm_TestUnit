
public class Operation {

	public static int addition(int x, int y) {
		
		return x + y;
	}
	
	public static int soustraction (int x, int y) {
		
		return x - y;
	}
	
	public static int multiplication (int x, int y) {
		
		return x * y;
	}

	public static int division (int x, int y) {
	
		if(y == 0) return 0;
		
		else return x / y;
			
	}

}
